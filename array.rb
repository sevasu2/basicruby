# 配列
# ============================================
# [要素1, 要素2, 要素3]
# 配列の要素にはさまざなデータ型を同時に入れることもできる
# ============================================

# 配列の作成
# ============================================
a = [1,2,3]
# ============================================


# 要素の変更 削除
# ============================================
a[1] = 20
# => 1 20 3

a[4] = 50
# => 1 20 3 nil 50
# ============================================

# 要素の追加
# ============================================
b = []
b << 1
b << 2
b << 3

# b = [1,2,3]
# ============================================

# 要素の削除
# ============================================
a.delete_at(1)
# => 1 3 50
# ============================================

# 配列を使った多重代入
# ============================================
a,b = [1 , 2]

c,d = [10]
# c #> 10
# d #> nil

e,f = [1 , 2 , 3]
# e => 1
# f => 2
# ============================================
